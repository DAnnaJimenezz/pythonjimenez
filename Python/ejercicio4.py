# Ingrese dos números enteros
num1 = int(input("Ingrese el primer número entero: "))
num2 = int(input("Ingrese el segundo número entero: "))

# Realizamos la multiplicación y la división de los dos números
multiplicacion = num1 * num2
division = num1 / num2

# Mostramos el resultado de la multiplicación y la división al usuario
print("La multiplicación de", num1, "y", num2, "es:", multiplicacion)
print("La división de", num1, "y", num2, "es:", division)
