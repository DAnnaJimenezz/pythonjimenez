
for i in range(11):   #Usa for y range para poner hasta que numero imprimir en este caso 11
    if i%3==0:   #Usa if para dar condicion de que sea multiplo de 3
        print(f'{i} es multiplo de 3')
    else:
        print(i)

for j in range(1,11):   #Utiliza for para definir cuantas veces ejecute el bucle un numero determinado de veces 
    print(j)

for k in range(0,11,2):    
    print(k)

for i in range(20,0,-2): 
    print(i)